package com.princetonsa.dto.facturacion;

public class DtoMontoCobro {
	
	private String codigoMonto;
	private String descripcion;
	
	
	public DtoMontoCobro() {
	}


	/**
	 * @return the codigoMonto
	 */
	public String getCodigoMonto() {
		return codigoMonto;
	}


	/**
	 * @param codigoMonto the codigoMonto to set
	 */
	public void setCodigoMonto(String codigoMonto) {
		this.codigoMonto = codigoMonto;
	}


	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}


	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	
	
}
